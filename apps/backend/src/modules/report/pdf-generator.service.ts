      doc.moveTo(360, rowY + 58).lineTo(555, rowY + 58).stroke('#2563EB');
      doc.fontSize(13).font('Helvetica-Bold').fillColor('#1E40AF');
      doc.text('TOTAL:', 360, rowY + 65, { width: 110, align: 'right' });
      doc.text(fmt(data.estimate.totalAmount), 475, rowY + 65, { width: 80, align: 'right' });

