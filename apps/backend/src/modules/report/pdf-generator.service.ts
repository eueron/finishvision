      doc.text(fmt(data.estimate.taxAmount), 485, rowY, { width: 75, align: 'right' });
      rowY += 4;
      doc.moveTo(380, rowY + 12).lineTo(562, rowY + 12).stroke('#2563EB');
      rowY += 20;
      doc.fontSize(12).font('Helvetica-Bold');
      doc.text('TOTAL:', 380, rowY, { width: 100, align: 'right' });
      doc.fillColor('#2563EB').text(fmt(data.estimate.totalAmount), 485, rowY, { width: 75, align: 'right' });

